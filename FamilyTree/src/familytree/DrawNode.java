package familytree;

public class DrawNode {

    public final String name;
    public final boolean male;
    public final int posX;
    public final int posY;
    
    public DrawNode(String name, boolean male, int posX, int posY) {
        this.name = name;
        this.male = male;
        this.posX = posX;
        this.posY = posY;
    }
    
}
