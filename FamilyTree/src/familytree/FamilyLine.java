package familytree;

public class FamilyLine {

    public final int startX;
    public final int startY;
    public final int endX;
    public final int endY;
    
    public FamilyLine(int startX, int startY, int endX, int endY) {
        this.startX = startX;
        this.startY = startY;
        this.endX = endX;
        this.endY = endY;
    }
    
}
